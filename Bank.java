public class Bank {

    int accid;
    String accName;
    int accBal;

    public int getAccid() {
        return this.accid;
    }

    public void setAccid(int accid) {
        this.accid = accid;
    }

    public String getAccName() {
        return this.accName;
    }

    public void setAccName(String accName) {
        this.accName = accName;
    }

    public int getAccBal() {
        return this.accBal;
    }

    public void setAccBal(int accBal) {
        this.accBal = accBal;
    }

       
}